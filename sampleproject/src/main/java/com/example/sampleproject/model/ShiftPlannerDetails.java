package com.example.sampleproject.model;

public class ShiftPlannerDetails {
	
	private int apacAnalystsCount;
	private int eocAnalystsCount;
	private int aocAnalystsCount;
	private String shiftDuration;
	private String status;
	public int getApacAnalystsCount() {
		return apacAnalystsCount;
	}
	public void setApacAnalystsCount(int apacAnalystsCount) {
		this.apacAnalystsCount = apacAnalystsCount;
	}
	public int getEocAnalystsCount() {
		return eocAnalystsCount;
	}
	public void setEocAnalystsCount(int eocAnalystsCount) {
		this.eocAnalystsCount = eocAnalystsCount;
	}
	public int getAocAnalystsCount() {
		return aocAnalystsCount;
	}
	public void setAocAnalystsCount(int aocAnalystsCount) {
		this.aocAnalystsCount = aocAnalystsCount;
	}
	public String getShiftDuration() {
		return shiftDuration;
	}
	public void setShiftDuration(String shiftDuration) {
		this.shiftDuration = shiftDuration;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}

}
